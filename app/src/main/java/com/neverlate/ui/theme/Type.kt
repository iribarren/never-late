import androidx.compose.ui.unit.sp

/**
 * Brand type scale (feature 16).
 *
 * Material 3 defines a full *type scale* — a fixed set of named text styles
 * (`displayLarge` down to `labelSmall`) that every screen picks from via
 * `MaterialTheme.typography.*`, instead of choosing raw font sizes ad hoc. That is what makes text
 * read as one designed system: a "titleLarge" looks the same wherever it appears, and the whole
 * scale can be retuned (or re-fonted) in this one file.
 *
 * This app deliberately stays on [FontFamily.Default] (the platform's system font) rather than
 * bundling a custom font under `res/font/` — a real `FontFamily` can still be built from TTF files
 * placed there with [androidx.compose.ui.text.font.Font] the same way, but a system font
 * already renders every language/script the device supports (important for feature 08's Spanish +
 * English strings) with zero APK size cost, so introducing one here would add a maintenance cost
 * without a matching visual-identity benefit. The brand identity instead comes from deliberate
 * per-role sizes/weights/line-heights below, and from the color scheme in Color.kt/Theme.kt.
 */
